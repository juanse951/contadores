/*Crea un contador que comience en 1 y vaya hasta 10 
usando un bucle 'while'. Muestra cada número.*/

let contador = 0;

while(contador != 10){
    contador++;
    //se mostrará en la consola el conteo de 1 a 10
    console.log(contador);
}

/*Crea un contador que comience en 10 y vaya hasta 0 
usando un bucle 'while'. Muestra cada número.*/

let contadorr = 11;

while(contadorr != 0){
    contadorr--;
    //se mostrará en la consola el conteo de 10 a 0
    console.log(contadorr);
}
    
